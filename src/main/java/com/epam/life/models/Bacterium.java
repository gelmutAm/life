package com.epam.life.models;

public class Bacterium {
    private int x;
    private int y;

    public Bacterium() {
    }

    public Integer getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}